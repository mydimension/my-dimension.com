			<h3><a href="/blog/2004/02/26/the-passion-of-the-christ/" rel="bookmark" title="The Passion of The Christ - Permalink">The Passion of The Christ</a></h3>
			<span class="postDate">February 26, 2004</span>
			<span class="postTime">09:40:06 -0500</span>
			
			<div class="body">
				<p>Last night I witnessed <a href="http://www.thepassionofthechrist.com/" title="The Passion of The Christ - A Mel Gibson Film - Official Movie Website">The Passion of The Christ</a>. This movie literally left me speechless. It is <em>so</em> moving. After the movie, I (along with nearly half of the audience in the theater) sat stunned, attempting to digest what I had just seen.</p>

	<p>There has been much debate as to whether this movie could raise anti-semtic feelings. I honestly believe this is not the case. Sure the Jewish high priests were the chief architects of Jesus&#8217; death, but if it had not been them it would have been someone else. Jesus came with the sole purpose of dying for <strong>all</strong> of <em>our</em> sins. In that light, we are <em>all</em> equally responsible for the death of Jesus. Unfortunately there are to many people that want to point the blame. To few who realize what Jesus&#8217; sacrifice means.</p>

	<p>Another issue that people have about this movie is the violent content. I feel it was absolutely warranted. Crucifiction was <em>the most brutal</em> form of punishment and death at the time and for many years following. That was life under the rule of the Roman Empire. Jesus&#8217; sacrifice would not have meant near has much as it does had he been afforded and easy death.</p>

	<p>Finally I would like to say that everyone should see this movie, and see it without any preconceived notions as to its content. Then, if you have not already done so, go and read the text that inspired the movie, the Bible.</p>

	<p>To Mel Gibson, thank you so much for putting forth this so poingant reminder of Jesus&#8217; sacrifice.</p>			</div>
			
